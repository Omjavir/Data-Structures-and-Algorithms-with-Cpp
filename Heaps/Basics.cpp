// ***** Maxheap *****
// If the root is the maximum of all the subarrays present in tree
// Example : 
// /*
//                 7
//                / \
//               5   4 
//              / \   
//             3   2
// */

// ***** Minheap *****
// If the root is the minimum of all the subarrays present in tree
// Example :
// /*
//                 1
//                / \
//               5   4 
//              / \   
//             3   2
// */